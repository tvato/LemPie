    @ColumnInfo("primaryFixedDim") val primaryFixedDim: Long = 0xFF000000,
    @ColumnInfo("onPrimaryFixed") val onPrimaryFixed: Long = 0xFFFFFFFF,
    @ColumnInfo("onPrimaryFixedVariant") val onPrimaryFixedVariant: Long = 0xFFF0F0F0,
    @ColumnInfo("secondaryFixed") val secondaryFixed: Long = 0xFF303030,
    @ColumnInfo("secondaryFixedDim") val secondaryFixedDim: Long = 0xFF060606,
    @ColumnInfo("onSecondaryFixed") val onSecondaryFixed: Long = 0xFFFFFFFF,
    @ColumnInfo("onSecondaryFixedVariant") val onSecondaryFixedVariant: Long = 0xFFF0F0F0,
