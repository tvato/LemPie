    @ColumnInfo("onSecondary") val onSecondary: Long = 0xFFFFFFFF,
    @ColumnInfo("secondaryContainer") val secondaryContainer: Long = 0xFF333333,
    @ColumnInfo("onSecondaryContainer") val onSecondaryContainer: Long = 0xFFFFFFFF,
    @ColumnInfo("tertiary") val tertiary: Long = 0xFF555555,
    @ColumnInfo("onTertiary") val onTertiary: Long = 0xFFFFFFFF,
    @ColumnInfo("tertiaryContainer") val tertiaryContainer: Long = 0xFFFFC107,
    @ColumnInfo("onTertiaryContainer") val onTertiaryContainer: Long = 0xFF000000,
    @ColumnInfo("background") val background: Long = 0xFF000000,
    @ColumnInfo("onBackground") val onBackground: Long = 0xFFFFFFFF,
    @ColumnInfo("surface") val surface: Long = 0xFF181818,
    @ColumnInfo("onSurface") val onSurface: Long = 0xFFFFFFFF,
    @ColumnInfo("surfaceVariant") val surfaceVariant: Long = 0xFFEA5050,
    @ColumnInfo("onSurfaceVariant") val onSurfaceVariant: Long = 0xFFF0F0F0,
    @ColumnInfo("surfaceTint") val surfaceTint: Long = 0xFF000000,
