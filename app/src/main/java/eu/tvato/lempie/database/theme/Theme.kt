    @ColumnInfo("onTertiaryFixed") val onTertiaryFixed: Long = 0xFFFFFFFF,
    @ColumnInfo("onTertiaryFixedVariant") val onTertiaryFixedVariant: Long = 0xFFF0F0F0,
