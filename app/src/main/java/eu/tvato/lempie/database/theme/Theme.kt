    @ColumnInfo("surfaceContainer") val surfaceContainer: Long = 0xFF303030,
    @ColumnInfo("surfaceContainerHigh") val surfaceContainerHigh: Long = 0xFF505050,
    @ColumnInfo("surfaceContainerHighest") val surfaceContainerHighest: Long = 0xFF535353,
